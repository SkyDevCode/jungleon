COMMENT ON COLUMN T_STAFF.TEL_NO IS '전화번호';
COMMENT ON COLUMN T_STAFF.FAX_NO IS 'FAX번호';
COMMENT ON COLUMN T_STAFF.CELL_NO IS '호출번호';
COMMENT ON COLUMN T_STAFF.STAFF_WORK IS '담당업무';
COMMENT ON COLUMN T_STAFF.STAFF_EMAIL IS '이메일';
COMMENT ON COLUMN T_STAFF.OLD_FILE IS '사진원본명';
COMMENT ON COLUMN T_STAFF.REG_DT IS '입사일자';
COMMENT ON COLUMN T_STAFF.STAFF_IMG IS '사진';
COMMENT ON COLUMN T_STAFF.STAFF_HP IS '휴대폰번호';
COMMENT ON COLUMN T_STAFF.STAFF_ORD IS '정렬순서';
COMMENT ON COLUMN T_STAFF.DELYN IS '삭제여부';
