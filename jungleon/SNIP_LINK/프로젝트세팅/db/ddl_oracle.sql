CREATE TABLE T_STAFF
(
  STAFF_NO     NUMBER(5)                        NOT NULL,
  STAFF_NM     VARCHAR2(50 BYTE)                NOT NULL,
  STAFF_DEPT   VARCHAR2(50 BYTE)                NOT NULL,
  STAFF_POS    VARCHAR2(50 BYTE)                NOT NULL,
  TEL_NO       VARCHAR2(30 BYTE),
  FAX_NO       VARCHAR2(30 BYTE),
  CELL_NO      VARCHAR2(30 BYTE),
  REG_DT       DATE,
  STAFF_WORK   VARCHAR2(4000 BYTE),
  STAFF_EMAIL  VARCHAR2(50 BYTE),
  STAFF_IMG    VARCHAR2(200 BYTE),
  STAFF_HP     VARCHAR2(30 BYTE),
  STAFF_ORD    NUMBER(4),
  OLD_FILE     VARCHAR2(200 BYTE),
  DELYN        VARCHAR2(2 BYTE)
);

